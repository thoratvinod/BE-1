#include <stdio.h>
#include <iostream>
#include <cstdlib>
#include <omp.h>
#define MAX 100
int main(){
	
	
	int a[MAX],b[MAX],c[MAX];
	#pragma omp parallel for
	for(int i=0;i<MAX;i++){
		a[i] = rand()%1000;
	}
	printf("\nFirst Vector : \n");
	for(int i=0;i<MAX;i++){
		printf("%d\t",a[i]);
	}
	#pragma omp parallel for
	for(int i=0;i<MAX;i++){
		b[i] = rand()%1000;
	}
	printf("\nSecond Vector : \n");
	for(int i=0;i<MAX;i++){
		printf("%d\t",b[i]);
	}
	
	printf("\nVector Addition of two Vectors :\n");
	#pragma omp parallel for
	for(int i=0;i<MAX;i++){
		c[i] = a[i]+b[i];
	}
	for(int i=0;i<MAX;i++){
		printf("%d\t%d\t%d\n",a[i],b[i],c[i]);
	}
	return 0;
}
